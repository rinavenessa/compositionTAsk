package com.rinavenessa.pcComposition;

public class Dimensions {

    private int width;
    private int height;
    private int breath;

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getBreath() {
        return breath;
    }

    public Dimensions(int width, int height, int breath) {
        this.width = width;
        this.height = height;
        this.breath = breath;
    }
}
